<?php
class Html {
    public static function format_week($week) {
         $WEEK_ARRAY = array(
        	0 => '日',
        	1 => '一',
        	2 => '二',
        	3 => '三',
        	4 => '四',
        	5 => '五',
        	6 => '六',
        );

        return $WEEK_ARRAY[$week];
    }

}